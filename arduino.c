int trigerpin =12;
int echopin = 11;
int buz = 7;


long duration;
int distance;
void setup() {
  Serial.begin(9600);
  pinMode(trigerpin,OUTPUT);
  pinMode(echopin,INPUT);
  pinMode(buz,OUTPUT);
  pinMode(5,OUTPUT);
  pinMode(6,OUTPUT);
}

void loop() {
  
  digitalWrite(trigerpin,LOW);
  delayMicroseconds(2);
  digitalWrite(trigerpin,HIGH);
  delayMicroseconds(10);
  digitalWrite(trigerpin,LOW);
  duration = pulseIn(echopin,HIGH);
  distance = duration*0.034/2;
  Serial.println(distance);
  if(distance>0 && distance<10)
  {
    tone(buz,255);
    digitalWrite(8,HIGH);
    digitalWrite(4,LOW);
    voice.say(spDANGER);
  voice.say(spDANGER);
  voice.say(spDANGER);
  digitalWrite(5,HIGH);
  digitalWrite(6,LOW);
        
  }
  if(distance>10 && distance<50)
  {
    tone(buz,30);
    digitalWrite(8,HIGH);
    digitalWrite(4,LOW);
    voice.say(spALERT);
    voice.say(spALERT);
  digitalWrite(5,HIGH);
  digitalWrite(6,LOW);
    
  }
  if(distance>50 && distance<1000)
  {
    noTone(buz
    );
    digitalWrite(4,HIGH);
    digitalWrite(8,LOW);
    digitalWrite(5,LOW);
  digitalWrite(6,LOW);
  }

}
